package com.idat.pe.Cus_Registro_Postulante.entity;

/**
 * Define el área de interés del postulante dentro del Club Náutico Neptuno.
 *
 * NAUTICO      → El postulante está orientado a participar en actividades
 *                náuticas: uso de amarraderos, embarcaciones del club, regattas, etc.
 *
 * INSTALACIONES → El postulante está orientado a hacer uso de las instalaciones
 *                 del club: piscinas, restaurante, zona social, deportes en tierra, etc.
 */
public enum TipoInteres {
    NAUTICO,
    INSTALACIONES
}
